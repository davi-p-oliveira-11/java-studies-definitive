public class Types {

    public static void main(String[] args) {
        // Integer types
        byte myByte = 100;
        short myShort = 10000;
        int myInt = 100000;
        long myLong = 10000000000L;

        // Floating point types
        float myFloat = 10.5f;
        double myDouble = 20.99;

        // Character type
        char myChar = 'A';

        // Boolean type
        boolean myBoolean = true;
    }

}
